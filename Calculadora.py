# Función para división Andres Charry
def dividir(a, b):
    if b != 0:
        return a / b
    else:
        raise ValueError("No se puede dividir por cero")
# Función para multiplicar Adrian Zarate
def multiplicar(a, b):
    return a * b
  
# Función para restar Cesar Arteaga
def restar(a, b):
    return a - b

# Función para sumar Brandon Lara
def suma(a, b):
    return a + b
